package handlers

import (
	"errors"
	"github.com/gin-gonic/gin"
	"github.com/konveyor/tackle-hub/db"
	"github.com/konveyor/tackle-hub/models"
	"gorm.io/gorm"
	"net/http"
)

//
// Routes
const (
	RoleBindingsRoot = "/role_bindings"
	RoleBindingParam = "role_binding"
	RoleBindingRoot  = RoleBindingsRoot + "/:" + RoleBindingParam
)

type RoleBindingHandler struct{}

func (h *RoleBindingHandler) AddRoutes(e *gin.Engine) {
	e.GET(RoleBindingsRoot, h.List)
	e.GET(RoleBindingsRoot+"/", h.List)
	e.POST(RoleBindingsRoot, h.Create)
	e.GET(RoleBindingRoot, h.Get)
	e.PUT(RoleBindingRoot, h.Update)
	e.DELETE(RoleBindingRoot, h.Delete)
}

func (h *RoleBindingHandler) Get(ctx *gin.Context) {
	model := models.RoleBinding{}
	id := ctx.Param(RoleBindingParam)
	result := db.DB.First(&model, "id = ?", id)
	if result.Error != nil {
		if errors.Is(result.Error, gorm.ErrRecordNotFound) {
			ctx.JSON(http.StatusNotFound, gin.H{
				"error": MsgNotFound,
			})
			return
		} else {
			ctx.JSON(http.StatusInternalServerError, gin.H{
				"error": MsgInternalServerError,
			})
			log.Error(result.Error, MsgInternalServerError)
			return
		}
	}
	ctx.JSON(http.StatusOK, model)
}

func (h *RoleBindingHandler) List(ctx *gin.Context) {
	var list []models.RoleBinding
	result := db.DB.Find(&list)
	if result.Error != nil {
		ctx.JSON(http.StatusInternalServerError, gin.H{
			"error": MsgInternalServerError,
		})
		log.Error(result.Error, MsgInternalServerError)
		return
	}

	ctx.JSON(http.StatusOK, list)
}

func (h *RoleBindingHandler) Create(ctx *gin.Context) {
	model := models.RoleBinding{}
	err := ctx.BindJSON(&model)
	if err != nil {
		ctx.JSON(http.StatusBadRequest, gin.H{
			"error": MsgBadRequest,
		})
		log.Error(err, MsgBadRequest)
		return
	}

	result := db.DB.Create(&model)
	if result.Error != nil {
		ctx.JSON(http.StatusInternalServerError, gin.H{
			"error": MsgInternalServerError,
		})
		log.Error(result.Error, MsgInternalServerError)
		return
	}
	ctx.JSON(http.StatusOK, model)
}

func (h *RoleBindingHandler) Delete(ctx *gin.Context) {
	id := ctx.Param(RoleBindingParam)

	result := db.DB.Delete(&models.RoleBinding{}, "id = ?", id)
	if result.Error != nil {
		if errors.Is(result.Error, gorm.ErrRecordNotFound) {
			ctx.Status(http.StatusOK)
			return
		} else {
			ctx.JSON(http.StatusInternalServerError, gin.H{
				"error": MsgInternalServerError,
			})
			log.Error(result.Error, MsgInternalServerError)
			return
		}
	}
	ctx.Status(http.StatusOK)
}

func (h *RoleBindingHandler) Update(ctx *gin.Context) {
	id := ctx.Param(RoleBindingParam)

	updates := models.RoleBinding{}
	err := ctx.BindJSON(&updates)
	if err != nil {
		ctx.JSON(http.StatusBadRequest, gin.H{
			"error": MsgBadRequest,
		})
		log.Error(err, MsgBadRequest)
		return
	}

	result := db.DB.Model(&models.RoleBinding{}).Where("id = ?", id).Omit("id").Updates(updates)
	if result.Error != nil {
		if errors.Is(result.Error, gorm.ErrRecordNotFound) {
			ctx.JSON(http.StatusNotFound, gin.H{
				"error": MsgNotFound,
			})
			return
		} else {
			ctx.JSON(http.StatusInternalServerError, gin.H{
				"error": MsgInternalServerError,
			})
			log.Error(result.Error, MsgInternalServerError)
			return
		}
	}
	ctx.Status(http.StatusOK)
}
